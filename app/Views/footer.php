<script>
$(document).ready(function () {
  $('.slider-wrapper').each(function () {
    let $sliderContainer = $(this).find('.slider-container');
    let $sliders = $sliderContainer.find('.image-slider');
    let currentIndex = 0;
    let totalSliders = $sliders.length;

    // Show the first slider initially
    $sliders.eq(currentIndex).addClass('active');

    function showNextSlide() {
      $sliders.eq(currentIndex).removeClass('active');
      currentIndex = (currentIndex + 1) % totalSliders;
      $sliders.eq(currentIndex).addClass('active');
    }

    // Auto slide every 4 seconds for each slider instance
    setInterval(showNextSlide, 4000);
  });
});


</script>
