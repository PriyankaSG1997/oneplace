<script>
    $(document).ready(function () {
        $('#productSearch').on('input', function () {
            var query = $(this).val().trim(); // Get the search query from the input field

            if (query) {
                $.ajax({
                    type: "GET", // Use GET request
                    url: "<?= base_url('get_product_and_shops'); ?>", // Ensure this matches your route
                    data: { search: query }, // Send the search term
                    dataType: 'json', // Ensure the response is treated as JSON
                    success: function (data) {
                        $('#productSuggestions').empty(); // Clear previous suggestions

                        if (data.length > 0) {
                            data.forEach(function (result) {
                                var listItem = $('<li role="presentation"></li>');
                                var link = $('<a role="menuitem" tabindex="-1"></a>');

                                // Check which field to display based on the data from the backend
                                if (result.productname) {
                                    link.text(result.productname); // For product data
                                } else if (result.vendor_name) {
                                    link.text(result.vendor_name); // For vendor data
                                } else if (result.name) {
                                    link.text(result.name); // For cities data
                                }

                                // On click, update the search field and hidden inputs
                                link.on('click', function () {
                                    $('#productSearch').val(link.text()); // Set the search input value
                                    $('#selectedId').val(result.id || result.product_id || result.vendor_id || result.city_id); // Set the selected ID
                                    $('#selectedName').val(link.text()); // Set the selected name
                                    $('#productSuggestions').hide(); // Hide the dropdown after selection
                                });

                                listItem.append(link);
                                $('#productSuggestions').append(listItem); // Append the list item to suggestions
                            });

                            $('#productSuggestions').show(); // Show the dropdown menu
                        } else {
                            $('#productSuggestions').hide(); // Hide the dropdown if no results
                        }
                    },
                    error: function (jqXHR, textStatus, errorThrown) {
                        console.log(textStatus, errorThrown); // Handle errors
                    }
                });
            } else {
                $('#productSuggestions').hide(); // Hide the dropdown if input is empty
            }
        });
    });

    // Function to handle search action
    function fetchProductData() {
        var selectedId = $('#selectedId').val();
        var selectedName = $('#selectedName').val();

        if (selectedId && selectedName) {
            // Redirect to the home page or a specific page with the selected ID and name as parameters
            window.location.href = "<?= base_url('home'); ?>?id=" + selectedId + "&name=" + encodeURIComponent(selectedName);
        } else {
            // If no product is selected, you can either show a message or perform another action
            alert('Please select a product from the list.');
        }
    }
